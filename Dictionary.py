# Key-Value pair

my_dictionary = {"Name":"Jack","Age":26}
print(my_dictionary)
type(my_dictionary)

# {'Name': 'Jack', 'Age': 26}
# dict

print(my_dictionary["Name"])
# Jack

# Dictionary doesn't allow duplicate values
my_dictionary2 = {"Name":"Jack","Age":26, "Name":"Jack","Name":"Jack"}
print(my_dictionary2)

# {'Name': 'Jack', 'Age': 26}

